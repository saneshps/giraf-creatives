        Swal.fire({
          title: 'Sending...',
          text: 'Please wait while we send your message.',
          allowOutsideClick: false,
          didOpen: () => {
            Swal.showLoading();
          }
        });

        form_btn.prop('disabled', true);

        $(form).ajaxSubmit({
          url: 'send_gmail.php',
          dataType: 'json',
          success: function (data) {
            Swal.close();

            form_btn.prop('disabled', false);
            if (data.status === 'success') {
                // alert('s')
                form.reset(); 
            //   $(form).find('.form-control').val('');
              Swal.fire({
                icon: 'success',
                title: 'Success!',
                text: data.message
              });
            } else {
                // alert('f')

              Swal.fire({
                icon: 'error',
                title: 'Oops...',
                text: data.message
              });
            }
          },
          error: function () {
            Swal.close();

            form_btn.prop('disabled', false);
            Swal.fire({
              icon: 'error',
              title: 'Oops...',
              text: 'Something went wrong. Please try again later.'
            });
          }
        });
      }
    });
  })(jQuery);
